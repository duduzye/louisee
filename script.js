// Carrossel de produtos - mostra um produto por vez
document.addEventListener("DOMContentLoaded", function() {
    const container = document.getElementById('produtos-carrossel');
    const items = container.querySelectorAll('.produto-item');
    const prev = document.getElementById('prod-prev');
    const next = document.getElementById('prod-next');
    let current = 0;

    function showSlide(idx) {
        items.forEach((item, i) => {
            item.classList.toggle('active', i === idx);
        });
    }
    showSlide(current);

    prev.addEventListener('click', () => {
        current = (current - 1 + items.length) % items.length;
        showSlide(current);
    });
    next.addEventListener('click', () => {
        current = (current + 1) % items.length;
        showSlide(current);
    });
});

