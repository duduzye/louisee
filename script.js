function translatePage(lang) {
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName.toLowerCase() === "title") {
                document.title = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });
    // Atualiza texto do botão de tema conforme idioma e modo
    const themeBtn = document.getElementById('theme-toggle');
    if (themeBtn) {
        const isDark = document.body.classList.contains('dark-mode');
        themeBtn.textContent = isDark ? translations[lang]['modo_claro'] : translations[lang]['modo_escuro'];
    }
}

document.addEventListener("DOMContentLoaded", () => {
    // Botão de idioma
    const langBtn = document.getElementById("lang-toggle");
    const langFlag = document.getElementById("lang-flag");
    const savedLang = localStorage.getItem("lang") || "pt";
    let currentLang = savedLang;

    function updateFlag() {
        langFlag.textContent = currentLang === "pt" ? "🇧🇷" : "🇺🇸";
        langFlag.setAttribute("aria-label", currentLang === "pt" ? "Português" : "English");
    }

    updateFlag();
    translatePage(currentLang);

    langBtn.addEventListener("click", () => {
        currentLang = currentLang === "pt" ? "en" : "pt";
        localStorage.setItem("lang", currentLang);
        updateFlag();
        translatePage(currentLang);

        // Atualiza texto do botão de tema se necessário
        const themeBtn = document.getElementById('theme-toggle');
        const isDark = document.body.classList.contains('dark-mode');
        themeBtn.textContent = isDark
            ? translations[currentLang]['modo_claro']
            : translations[currentLang]['modo_escuro'];
    });

    // --- MODO ESCURO PERSISTENTE ---
    const themeBtn = document.getElementById('theme-toggle');
    const savedTheme = localStorage.getItem("theme");
    if (savedTheme === "dark") {
        document.body.classList.add('dark-mode');
        themeBtn.textContent = translations[currentLang]['modo_claro'];
    } else {
        document.body.classList.remove('dark-mode');
        themeBtn.textContent = translations[currentLang]['modo_escuro'];
    }

    themeBtn.addEventListener('click', () => {
        document.body.classList.toggle('dark-mode');
        const isDark = document.body.classList.contains('dark-mode');
        localStorage.setItem("theme", isDark ? "dark" : "light");
        themeBtn.textContent = isDark
            ? translations[currentLang]['modo_claro']
            : translations[currentLang]['modo_escuro'];
    });
});

    // Validação simples do formulário de contato
    document.addEventListener("DOMContentLoaded", function() {
        const form = document.getElementById("contato-form");
        if (form) {
            form.addEventListener("submit", function(e) {
                const nome = document.getElementById("nome").value.trim();
                const mensagem = document.getElementById("mensagem").value.trim();
                if (!nome || !mensagem) {
                    alert("Por favor, preencha todos os campos.");
                    e.preventDefault();
                }
            });
        }
    });

// Carrossel de produtos - mostra um produto por vez
document.addEventListener("DOMContentLoaded", function() {
    const container = document.getElementById('produtos-carrossel');
    const items = container.querySelectorAll('.produto-item');
    const prev = document.getElementById('prod-prev');
    const next = document.getElementById('prod-next');
    let current = 0;

    function showSlide(idx) {
        items.forEach((item, i) => {
            item.classList.toggle('active', i === idx);
        });
    }
    showSlide(current);

    prev.addEventListener('click', () => {
        current = (current - 1 + items.length) % items.length;
        showSlide(current);
    });
    next.addEventListener('click', () => {
        current = (current + 1) % items.length;
        showSlide(current);
    });
});

// Menu responsivo com animação de transição
document.addEventListener("DOMContentLoaded", function() {
    const menuToggle = document.getElementById('menu-toggle');
    const menuList = document.getElementById('menu-list');
    if (menuToggle && menuList) {
        menuToggle.addEventListener('click', function() {
            menuList.classList.toggle('open');
        });
        // Fecha o menu ao clicar em um link (opcional)
        menuList.querySelectorAll('a').forEach(link => {
            link.addEventListener('click', () => menuList.classList.remove('open'));
        });
    }
});

// Animação de zoom ao passar o mouse nos produtos do cardápio
document.addEventListener("DOMContentLoaded", function() {
    document.querySelectorAll('.bolo-item img, .brigadeiro-item img').forEach(function(img) {
        img.addEventListener('mouseenter', function() {
            img.classList.add('zoom');
        });
        img.addEventListener('mouseleave', function() {
            img.classList.remove('zoom');
        });
    });
});

